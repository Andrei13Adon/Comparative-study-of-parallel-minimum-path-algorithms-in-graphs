package AlgoritmRulare.Paralel.DelataSteppingSingle.communResorc;

/**
 *
 * @author Mocioi Andrei Adonis
 */
public class Interval {
    int start;
    int end;
    
    public Interval(){
        start = -1;
        end = -1;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
    
}
